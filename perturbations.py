import numpy as np
import os
from scipy.special import hyp2f1
from scipy.integrate import simpson
from scipy.interpolate import RegularGridInterpolator
from .transfer_EHfit import transferfunction_EisensteinHu

c = 299792.458 # km/s

def W(x):
  '''Top-hat window function in Fourier space.'''
  return np.piecewise(x,[x<=0.16],[lambda x: 1 - x**2/10. + x**4/280. - x**6/15120. + x**8/1330560. - x**10/172972800.,lambda x: 3/x**3 * (np.sin(x)-x*np.cos(x))])

def sigma8(k,P,h):
  '''
  Evaluate sigma_8 given tabulated dimensionless power spectrum P as a function
  of k in Mpc^-1. h is the Hubble parameter.
  '''
  return np.sqrt(simpson(P*W(k*8./h)**2,x=np.log(k)))

def growth_late(a,OmegaM,f_nc):
  '''
  
  Linear-order growth function in a spatially flat matter/dark energy-dominated
  universe.
  
  Parameters:
    
    a: float or array
      The scale factor.
      
    OmegaM: float
      The matter density parameter. We assume OmegaLambda = 1-OmegaM.
      
    f_nc: float
      Fraction of the matter that does not contribute to clustering (e.g.,
      baryons below the Jeans length scale).
      
  Returns:
    
    D(a): float or array
  
  '''
  g = 1.25 * np.sqrt(1.-0.96*f_nc) - 0.25
  x = (1.-OmegaM)/OmegaM * a**3
  return a**g * hyp2f1(g/3.,(g+2.)/3.,(7.+4.*g)/6.,-x)

class Transfer_table(object):
  '''
  Transfer function generated by CLASS with Planck (2018) parameters.
  
  Call this object with parameters (k,a), where k is wavenumber and a is scale
  factor, to return abs(T_CDM), abs(T_baryon), abs(T_matter). The density power
  spectrum is T^2 P_zeta where P_zeta is the primordial curvature power spectrum.
  '''
  def __init__(self,):
    current_path = os.path.realpath(os.path.join(os.getcwd(), os.path.dirname(__file__)))
    data = np.load(current_path + '/data/transfer.npz')
    self.lnk = np.log(data['k'])
    self.lna = np.log(data['a'])
    self.lnT = np.log(np.abs(data['T'])).T
    self.__interpolator = RegularGridInterpolator((self.lnk,self.lna),self.lnT)
  
  def __call__(self,k,a=1.):
    T = np.exp(self.__interpolator((np.log(k),np.log(a)),))
    return tuple(t.T for t in T.T)

def transfer_EisensteinHu(k,h,OmegaM,OmegaB):
  '''
  Transfer function using Eisenstein & Hu fit.
  
  Parameters:
    
    k: float or array
    
    h, OmegaM, OmegaB: floats
      Cosmological parameters.
      
  Returns:
    
    abs(T_CDM), abs(T_baryon), abs(T_matter): floats or arrays
      Density power spectrum is T^2 P_zeta where P_zeta is the primordial
      curvature power spectrum.
  '''
  H = 100*h/c # Mpc^-1
  return tuple((k/H)**2*T for T in transferfunction_EisensteinHu(k,OmegaM*h**2,OmegaB/OmegaM,0.))
